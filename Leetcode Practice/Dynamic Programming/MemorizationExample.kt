fun fibSeq(n:Int, result:IntArray):Int {
    if(n <= 1) {
        result[n] = n
        return n
    }

    println(result.toTypedArray().contentToString())

    if(result[n] != -1) return result[n]

    val ans1 = fibSeq(n-1,result)
    val ans2 = fibSeq(n-2,result)
    result[n] = ans1 + ans2
    return result[n]
}

